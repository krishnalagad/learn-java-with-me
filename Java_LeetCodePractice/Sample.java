public class Sample {
    /**
     * @param n : number of candles and it is also the price of one candle.
     * @return : price of all candles ravi has bought from shopkeeper
     */
    public static int solve(int n){
        return n * n;
    }
    
    public static void main(String[] args) {
        System.out.println(solve(4));
    }
}
